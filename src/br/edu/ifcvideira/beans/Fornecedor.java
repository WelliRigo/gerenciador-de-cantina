package br.edu.ifcvideira.beans;

public class Fornecedor {
	private int idFr;
	private String nomeFr;
	private String cnpjFr;
	
	public int getIdFr() {
		return idFr;
	}
	public void setIdFr(int idFr) {
		this.idFr = idFr;
	}
	public String getNomeFr() {
		return nomeFr;
	}
	public void setNomeFr(String nomeFr) {
		this.nomeFr = nomeFr;
	}
	public String getCnpjFr() {
		return cnpjFr;
	}
	public void setCnpjFr(String cnpjFr) {
		this.cnpjFr = cnpjFr;
	}
}
