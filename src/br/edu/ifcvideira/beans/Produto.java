package br.edu.ifcvideira.beans;

public class Produto {
	private int idPr;
	private String nomePr;
	private double valorUnitarioPr;
	
	public int getIdPr() {
		return idPr;
	}
	public void setIdPr(int idPr) {
		this.idPr = idPr;
	}
	public String getNomePr() {
		return nomePr;
	}
	public void setNomePr(String nomePr) {
		this.nomePr = nomePr;
	}
	public double getValorUnitarioPr() {
		return valorUnitarioPr;
	}
	public void setValorUnitarioPr(double valorUnitarioPr) {
		this.valorUnitarioPr = valorUnitarioPr;
	}
	
	
}
